#basic arithmetic operation

#add 
friends = 5
friends = friends + 1
friends += 1 #same thing as line above just shorter/easier to type 
print(friends)

#subtract
friends = 5
friends -= 2
print(friends)

#multiplication
friends = 5
friends *= 3
print(friends)

#divide
friends = 5
friends /= 2
print(friends)

# to the power
friends = 5
friends **= 2
print(friends)

#remainder
friends = 5
remainder = friends % 3
print(remainder)
